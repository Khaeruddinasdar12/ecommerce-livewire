 <h4>#Php</h4>
 <p class="text-justify">Aplikasi ini dibangun pada bahasa pemrograman PHP7, php sendiri mudah dipahami dan diimplementasikan sehingga penulis memilih bahasa ini sebagai Back End dari aplikasi e-commerce ini. Bahasa Php juga cukup stabil dan telah digunakan oleh perusahaan besar untuk membuat web dinamis, contoh perusahaan yang menggunakan php sebagai Back End yaitu Facebook, Wordpress dan berbagai CMS lainnya </p>

 <p class="text-justify">PHP adalah bahasa pemrograman yang sering disisipkan ke dalam HTML. PHP sendiri berasal dari kata Hypertext Preprocessor. Sejarah PHP pada awalnya merupakan kependekan dari Personal Home Page (Situs personal). PHP pertama kali dibuat oleh Rasmus Lerdorf pada tahun 1995. Pada waktu itu PHP masih bernama Form Interpreted (FI), yang wujudnya berupa sekumpulan skrip yang digunakan untuk mengolah data formulir dari web.</p>

 <p class="text-justify">Bahasa pemrograman ini menggunakan sistem server-side. Server-side programming adalah jenis bahasa pemrograman yang nantinya script/program tersebut akan dijalankan/diproses oleh server. Kelebihannya adalah mudah digunakan, sederhana, dan mudah untuk dimengerti dan dipelajari.</p>

 <p class="text-justify">Semenjak PHP menjadi bahasa pemrograman yang open source, pengembang tidak perlu menunggu sampai dengan update terbaru rilis.</p>

 <p class="text-justify">Pengguna PHP akan lebih baik jika menggunakan versi terbaru. Sehingga jika ada rilis terbaru Anda harus menyesuaikan sistem Anda dengan versi PHP yang paling baru. Meskipun harus menggunakan versi terbaru, biaya untuk maintenance dan web development sangat terjangkau.</p>

 <p class="text-justify">Bahasa pemrograman PHP membantu Anda untuk mengembangkan aplikasi berbasis web yang cukup kompleks, handal, dan cepat. Tergantung dari spesifikasi bisnis, penggunaan hosting, tingkat pengalaman, kebutuhan aplikasi, dan pengembangan timeframe. Selain itu ada banyak PHP frameworks yang dapat Anda pilih.</p>
