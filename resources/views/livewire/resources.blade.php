 <h4>#Javascript</h4>
 <p class="text-justify">Aplikasi ini dibangun pada bahasa javascript untuk membuatnya lebih dinamis, jadi bukan hanya bahasa php saja tetapi javascript, html, dan css juga terlibat dalam pembuatan aplikasi ini sebab ketiga hal tersebut memang komponen utama yang harus ada dalam sebuah aplikasi web untuk membuatnya lebih dinamis</p>

 <p class="text-justify">Jadi javascript merupakan salah satu bahasa pemrograman web yang bisa membuat halaman website anda lebih menarik serta lebih hidup. Bahasa pemrograman javascript itu dijalankan di perangkat pengunjung website atau situs, bukan di server jadi berbeda dengan PHP. </p>

 <p class="text-justify">Javascript juga dikatakan berbeda dari HTML dan CSS. HTML itu fungsinya untuk mengatur tampilan konten dan CSS mengelola layoutnya jadi ketiganya sangatlah berbeda. Banyak yang mengibaratkan perbandingan HTML, CSS, dan javascript seperti bangunan rumah, perabotan di dalamnya, serta lampu dan pintu-pintu.</p>

 <p class="text-justify">Jadi, HTML itu diibaratkan sebagai bangunan rumah yang digunakan untuk pondasi semua hal seperti tempat berdirinya tembok, pintu, atap, dan lain sebagainya. Sedangkan CSS itu adalah perabotan hiasan yang mampu memperindah rumah sepeti hiasan rumah, wallpaper, karpet, dan lain sebagainya. Nah javascript berperan sebagai hal yang menghidupkan dan responsif seperti bisa untuk menyalakan lampu atau membukakan pintu.</p>

 <p class="text-justify">Javascript membuat website menjadi lebih responsif jadi apabila tidak ada tidak masalah tapi akan sangat berpengaruh pada tampilan website. Anda bisa mengibaratkan pada rumah Anda. Mungkin tidak masalah tidak menggunakan lampu atau tidak dipasang pintu tapi tentu kesannya rumah akan suram dan kurang menarik. Begitu pula dengan website, tidak adanya javascript membuat website kurang menarik.</p>
