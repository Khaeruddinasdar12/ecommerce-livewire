 <h4>#Shopping Cart</h4>
 <p class="text-justify">Shopping Cart atau keranjang belanja adalah halaman untuk menampung berbagai macam belanjaan Anda, sehingga Anda dapat belanja berbagai produk dari berbagai kategori dalam toko online ini, kemudian menjumlahkannya dalam 1 nota atau tagihan atau invoice sehingga dalam berbagai belanjaan dapa terhitung sebagai satu transaksi. Anda dapat menyimpan belanjaan Anda pada fitur Keranjang belanja ini walaupun belum login, namun untuk checkout tentu Anda harus registrasi dan login pada aplikasi ini.	 </p>

 <p class="text-justify">Aplikasi ini memungkinkan pelanggan belanja online untuk mengumpulkan daftar item untuk pembelian, digambarkan kiasan sebagai "menempatkan item dalam belanja troli" atau "add to cart". Setelah checkout, perangkat lunak biasanya menghitung total untuk pesanan, termasuk pengiriman dan penanganan (yaitu, ongkos kirim dan packing) biaya dan pajak yang terkait, sebagaimana berlaku.</p>

<p class="text-justify">Di dalam sebuah situs toko online wajib memiliki keranjang belanja yang berfungsi, di karenakan untuk mempermudah semua transaksi pembelian. Anda bisa bayangkan apabila sebuah toko online tidak memiliki keranjang belanja? tanpa keranjang belanja Anda hanya dapat membeli satu barang untuk sekali transaksi artinya ketika melakukan pembelian hanya maksimal 1 barang saja yang bisa dibeli, Anda dapat membeli lagi pada pembelian berikutnya. </p>
